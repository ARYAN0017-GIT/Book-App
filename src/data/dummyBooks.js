export const getPopularBooks = () => {
  return dummyBooks.filter(book => book.isPopular).slice(0, 6);
};

export const getBooksByCategory = (category) => {
  if (category === 'all') return dummyBooks;
  return dummyBooks.filter(book => book.category === category);
};

export const getBookById = (id) => {
  return dummyBooks.find(book => book.id === parseInt(id));
};

export const searchBooks = (query, category = 'all') => {
  let books = category === 'all' ? dummyBooks : getBooksByCategory(category);
  
  if (!query) return books;
  
  const searchTerm = query.toLowerCase();
  return books.filter(book => 
    book.title.toLowerCase().includes(searchTerm) ||
    book.author.toLowerCase().includes(searchTerm) ||
    book.description.toLowerCase().includes(searchTerm)
  );
};